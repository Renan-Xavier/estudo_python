# Lista vazia com input para adicionar items

lista = []                                # cria uma lista vazia

item = input("Digite um item da lista: ") # solicita ao usuário que digite um item

lista.append(item)                        # adiciona o item à lista

print("Lista atual:", lista)              # exibe a lista atualizada

# só possivel adicionar 1 item a lista, pois para adicionar mais, teria que adicionar um loop.

