# metodos de listas e seus usos
# 1. append: adiciona um elemento ao final da lista 
# 2. insert: adiciona um elemento em uma posição específica da lista
# 3. remove: remove a primeira ocorrência de um elemento na lista
# 4. pop: remove e retorna o elemento em uma posição específica da lista
# 5. sort: ordena os elementos da lista em ordem crescente
# 6. reverse: inverte a ordem dos elementos na lista
# 7. count: conta o número de ocorrências de um elemento na lista
# 8. index: retorna o índice da primeira ocorrência de um elemento na lista
# 9. clear: remove todos os elementos da lista
# 10. extend: adiciona os elementos de uma lista a outra lista
# 11. copy: cria uma cópia da lista
# 12. len: retorna o número de elementos na lista
# 13. min: retorna o menor elemento da lista
# 14. max: retorna o maior elemento da lista
# 15. sum: retorna a soma dos elementos da lista
# 16. list: cria uma lista a partir de um iterável (como uma string ou uma tupla)
# 17. join: une os elementos de uma lista em uma string, separando-os por um delimitador especificado
# 18. split: divide uma string em uma lista, usando um delimitador especificado
# 19. slice: cria uma sublista a partir de uma lista, especificando um intervalo de índices
# 20. enumerate: retorna um objeto enumerado, que contém pares de índice e valor
# 21. zip: cria um iterador que agrega elementos de várias listas em tuplas
# 22. map: aplica uma função a cada elemento de uma lista e retorna um iterador com os resultados
# 23. filter: filtra os elementos de uma lista com base em uma função e retorna um iterador com os elementos que atendem ao critério
# 24. reduce: aplica uma função cumulativa aos elementos de uma lista, reduzindo-a a um único valor (disponível no módulo functools)
# 25. any: retorna True se qualquer elemento da lista for verdadeiro
# 26. all: retorna True se todos os elementos da lista forem verdadeiros
# 27. sorted: retorna uma nova lista ordenada a partir dos elementos de uma lista existente
# 28. reversed: retorna um iterador que percorre os elementos de uma lista em ordem reversa
# 29. list comprehension: uma forma concisa de criar listas usando uma expressão e um laço for
# 30. slicing with step: permite criar sublistas com um passo específico, pulando elementos
# 31. negative indexing: permite acessar elementos de uma lista a partir do final, usando índices negativos
# 32. nested lists: listas que contêm outras listas como elementos, permitindo a criação de estruturas de dados mais complexas
# 33. multidimensional lists: listas que representam matrizes ou tabelas, onde cada elemento é uma lista
# 34. list unpacking: atribuição de elementos de uma lista a variáveis individuais
# 35. list concatenation: combinação de duas ou mais listas em uma única lista usando o operador +
# 36. list multiplication: criação de uma lista repetida várias vezes usando o operador *
